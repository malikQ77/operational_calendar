
class Reminder {
  String reminderName;
  int reminderColor;
  String reminderDate;
  String time;


  Reminder(reminderName, reminderColor ,reminderDate , time) {
    this.reminderName = reminderName;
    this.reminderColor = reminderColor;
    this.reminderDate = reminderDate;
    this.time = time;
  }
}