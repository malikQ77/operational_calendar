
class Reminder {
  String reminderName;
  int reminderColor;
  String reminderDate;
  String reminderTime;


  Reminder(reminderName, reminderColor ,reminderDate , reminderTime) {
    this.reminderName = reminderName;
    this.reminderColor = reminderColor;
    this.reminderDate = reminderDate;
    this.reminderTime = reminderTime;
  }
}